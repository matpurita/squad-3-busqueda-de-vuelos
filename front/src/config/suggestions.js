// Función para obtener sugerencias aleatorias
export const getRandomSuggestions = (maxResults = 3, excludeDestinations = []) => {
  const filteredSuggestions = suggestionsConfig.filter(
    suggestion => !excludeDestinations.includes(suggestion.destination)
  );
  
  // Mezclar aleatoriamente y devolver los primeros resultados
  const shuffled = filteredSuggestions.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, maxResults);
};

// Función para obtener sugerencias por tag/tipo
export const getSuggestionsByTag = (tag, maxResults = 3) => {
  const filtered = suggestionsConfig.filter(
    suggestion => suggestion.tag.toLowerCase().includes(tag.toLowerCase())
  );
  return filtered
    .sort((a, b) => a.priority - b.priority)
    .slice(0, maxResults);
};

// Función para obtener sugerencias por región/precio
export const getSuggestionsByPriceRange = (maxPrice, maxResults = 3) => {
  const filtered = suggestionsConfig.filter(suggestion => {
    const price = parseInt(suggestion.price.replace(/[^0-9]/g, ''));
    return price <= maxPrice;
  });
  
  return filtered
    .sort((a, b) => {
      const priceA = parseInt(a.price.replace(/[^0-9]/g, ''));
      const priceB = parseInt(b.price.replace(/[^0-9]/g, ''));
      return priceA - priceB;
    })
    .slice(0, maxResults);
};

// Función para obtener todas las sugerencias
export const getAllSuggestions = () => {
  return suggestionsConfig.sort((a, b) => a.priority - b.priority);
};

// Función para obtener una sugerencia específica por ID
export const getSuggestionById = (id) => {
  return suggestionsConfig.find(suggestion => suggestion.id === id);
};

// Función para obtener sugerencias excluyendo el destino actual
export const getSuggestionsExcluding = (currentDestination, maxResults = 3) => {
  const filtered = suggestionsConfig.filter(
    suggestion => suggestion.destination !== currentDestination
  );
  
  // Mezclar aleatoriamente
  const shuffled = filtered.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, maxResults);
};

// Función legacy para compatibilidad con el componente actual
export const getSuggestionsByCondition = (condition, maxResults = 3) => {
  // Mapear condiciones a tags para mantener compatibilidad
  const conditionToTagMap = {
    'popular': 'Popular',
    'international': 'Cultural',
    'national': 'Nacional',
    'business': 'Business',
    'alternative': 'Aventura',
    'seasonal': 'Histórico'
  };
  
  const tag = conditionToTagMap[condition];
  if (tag) {
    return getSuggestionsByTag(tag, maxResults);
  }
  
  // Si no encuentra la condición, devolver sugerencias aleatorias
  return getRandomSuggestions(maxResults);
};